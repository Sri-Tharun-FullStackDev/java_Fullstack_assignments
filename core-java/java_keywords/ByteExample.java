public class ByteExample {
    public static void main(String[] args) {
        // Declaration and initialization of byte variables
        byte num1 = 10;
        byte num2 = 20;

        // Adding two byte variables
        byte sum = (byte) (num1 + num2);

        // Subtracting two byte variables
        byte difference = (byte) (num1 - num2);

        // Multiplying two byte variables
        byte product = (byte) (num1 * num2);

        // Dividing two byte variables
        byte quotient = (byte) (num1 / num2);

        // Printing the results
        System.out.println("num1: " + num1);
        System.out.println("num2: " + num2);
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);
    }
}
