public class Swap_two_numbers {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c;

        System.out.println("number in A before swap: "+a);
        System.out.println("number in B before swap: "+b);


        //swaping two numbers
        c=a;
        a=b;
        b=c;

        //after swapping two numbers

        System.out.println("number in A after swap: "+a);
        System.out.println("number in B after swap: "+b);




    }
    
}
