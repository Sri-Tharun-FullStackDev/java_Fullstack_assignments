
// switch is also a conditional statement and gets executed in a order. 
// switch is used in the occation when there is necessary multiple cases.


let color = "red";


switch (color) {
    case "red":
        console.log("Color is red");
        break;
    case "blue":
        console.log("Color is blue");
        break;
    default:
        console.log("Unknown color");


    }
